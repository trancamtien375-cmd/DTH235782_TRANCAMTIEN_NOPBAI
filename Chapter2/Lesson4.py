'''
1. Kiểu số (Numeric types)

int: số nguyên (vd: -5, 0, 100).

float: số thực dấu phẩy động (vd: 3.14, -0.5).

complex: số phức (vd: 2 + 3j).

2. Kiểu logic (Boolean type)

bool: chỉ có hai giá trị True hoặc False.

3. Kiểu chuỗi (Text type)

str: chuỗi ký tự (vd: "Xin chào", 'Python').

4. Kiểu tập hợp (Sequence types)

list: danh sách (vd: [1, 2, 3]).

tuple: bộ giá trị (vd: (1, "a", 3.5)).

range: dãy số (vd: range(5) → 0,1,2,3,4).

5. Kiểu tập hợp không có thứ tự (Set types)

set: tập hợp không trùng lặp (vd: {1, 2, 3}).

frozenset: tập hợp bất biến (giống set nhưng không thay đổi được).

6. Kiểu ánh xạ (Mapping type)

dict: từ điển, lưu trữ theo cặp key: value (vd: {"ten": "Tiên", "tuoi": 20}).

7. Kiểu đặc biệt (Special type)

NoneType: chỉ có một giá trị duy nhất là None (thường dùng để biểu thị “không có gì”).
'''